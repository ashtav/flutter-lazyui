/// A model class representing a checkbox value.
class CheckboxValue {
  /// The label text of the checkbox.
  final String label;

  /// The value associated with the checkbox.
  final dynamic value;

  /// Constructs a [CheckboxValue].
  ///
  /// [label] : The label text of the checkbox.
  ///
  /// [value] : The value associated with the checkbox.
  CheckboxValue(this.label, {this.value});
}
