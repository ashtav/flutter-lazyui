import 'package:flutter/material.dart';

class FormStyle {
  final Color? background, borderColor;
  final double? radius;
  final IconData? prefixIcon, suffixIcon;
  final Color? prefixIconColor, suffixIconColor, textColor;

  FormStyle(
      {this.background,
      this.borderColor,
      this.radius,
      this.prefixIcon,
      this.suffixIcon,
      this.prefixIconColor,
      this.suffixIconColor,
      this.textColor});
}
